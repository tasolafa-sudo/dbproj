def next_id(cursor, table, column, prefix, digits=3):
    cursor.execute(f"LOCK TABLES {table} WRITE")
    cursor.execute(f"SELECT MAX({column}) AS max_id FROM {table}")
    row = cursor.fetchone()
    max_id = row["max_id"] if row else None
    if not max_id:
        return f"{prefix}{1:0{digits}d}"
    n = int(str(max_id)[len(prefix):]) + 1
    return f"{prefix}{n:0{digits}d}"


def to_bool(value):
    return str(value).lower() in {"1", "true", "on", "yes"}


def safe_date(value):
    if not value:
        return None
    try:
        return datetime.strptime(value, "%Y-%m-%d").date()
    except ValueError:
        return None


_FIELD_MAX_LENGTHS = {
    "username": 50, "password": 255, "company_id": 6, "company_name": 100,
    "employee_name": 50, "project_name": 100, "site_name": 50, "location": 100,
}


def validate_lengths(**fields):
    """Return an error string if any field exceeds its DB column length, else None."""
    for field, value in fields.items():
        max_len = _FIELD_MAX_LENGTHS.get(field)
        if max_len and value and len(str(value)) > max_len:
            label = field.replace("_", " ").title()
            return f"{label} must be {max_len} characters or fewer."
    return None


def is_invalid_date_range(start_date, end_date):
    return bool(start_date and end_date and end_date < start_date)


def safe_float(value, default=None):
    try:
        return float(value) if value not in (None, "") else default
    except (TypeError, ValueError):
        return default


