@app.route("/register", methods=["GET", "POST"])
def register():
    with get_cursor() as cur:
        cur.execute("SELECT CompanyID, CompanyName FROM Company ORDER BY CompanyName")
        companies = cur.fetchall()

    if request.method == "POST":
        company_id = request.form.get("company_id", "").strip()
        company_auth_code = request.form.get("company_auth_code", "")
        username = request.form.get("username", "").strip()
        password = request.form.get("password", "")
        confirm = request.form.get("confirm_password", "")

        if password != confirm:
            flash("Passwords do not match.", "danger")
            return render_template("register.html", companies=companies)

        if len(password) < 8:
            flash("Password must be at least 8 characters.", "danger")
            return render_template("register.html", companies=companies)

        err = validate_lengths(username=username)
        if err:
            flash(err, "danger")
            return render_template("register.html", companies=companies)

        with get_conn() as conn:
            cur = conn.cursor(dictionary=True)
            cur.execute("SELECT AuthCodeHash FROM CompanyAuthCode WHERE CompanyID = %s", (company_id,))
            auth_row = cur.fetchone()
            if not auth_row or not check_password_hash(auth_row["AuthCodeHash"], company_auth_code):
                flash("Invalid company auth code.", "danger")
                cur.close()
                return render_template("register.html", companies=companies)

            cur.execute("SELECT COUNT(*) AS cnt FROM User_tbl WHERE Username = %s", (username,))
            if cur.fetchone()["cnt"] > 0:
                flash("Username already exists.", "danger")
                cur.close()
                return render_template("register.html", companies=companies)

            user_id = next_id(cur, "User_tbl", "UserID", "USR", 3)
            hashed = generate_password_hash(password)
            cur.execute("UNLOCK TABLES")
            cur.execute(
                "CALL create_account(%s, %s, %s, %s)",
                (user_id, company_id, username, hashed),
            )
            cur.close()

        flash("Account created. Please log in.", "success")
        return redirect(url_for("login"))

    return render_template("register.html", companies=companies)


