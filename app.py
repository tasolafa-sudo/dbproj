def ensure_password_column_size():
    """
    Ensure User_tbl.Password can store hashed passwords.
    Older schema versions use VARCHAR(50), which truncates hashes.
    """
    try:
        with get_conn() as conn:
            cur = conn.cursor(dictionary=True)
            cur.execute(
                """
                SELECT CHARACTER_MAXIMUM_LENGTH AS max_len
                FROM information_schema.COLUMNS
                WHERE TABLE_SCHEMA = DATABASE()
                  AND TABLE_NAME = 'User_tbl'
                  AND COLUMN_NAME = 'Password'
                """
            )
            row = cur.fetchone()
            if row and row.get("max_len") and int(row["max_len"]) < 255:
                cur.execute("ALTER TABLE User_tbl MODIFY Password VARCHAR(255) NOT NULL")
            cur.close()
    except Exception as exc:
        app.logger.warning("Could not verify/upgrade User_tbl.Password column: %s", exc)


def ensure_company_auth_table():
    """
    Ensure company auth-code table exists for gated self-registration.
    Auth codes are stored hashed.
    """
    try:
        with get_conn() as conn:
            cur = conn.cursor(dictionary=True)
            cur.execute(
                """
                CREATE TABLE IF NOT EXISTS CompanyAuthCode (
                    CompanyID VARCHAR(50) PRIMARY KEY,
                    AuthCodeHash VARCHAR(255) NOT NULL,
                    CONSTRAINT fk_company_auth_company
                      FOREIGN KEY (CompanyID) REFERENCES Company(CompanyID)
                      ON DELETE CASCADE
                )
                """
            )
            cur.close()
    except Exception as exc:
        app.logger.warning("Could not verify/create CompanyAuthCode table: %s", exc)


